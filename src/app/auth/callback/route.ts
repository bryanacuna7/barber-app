import { NextRequest, NextResponse } from 'next/server'
import { createServerClient } from '@supabase/ssr'
import { cookies } from 'next/headers'

export async function GET(request: NextRequest) {
  const { searchParams } = new URL(request.url)
  const code = searchParams.get('code')
  const error = searchParams.get('error')
  const errorDescription = searchParams.get('error_description')

  const appUrl = process.env.NEXT_PUBLIC_APP_URL || 'https://barber-app-umber.vercel.app'

  // Handle error from Supabase (expired link, etc.)
  if (error) {
    const loginUrl = new URL('/login', appUrl)
    loginUrl.searchParams.set('error', errorDescription || 'Error de autenticación')
    return NextResponse.redirect(loginUrl)
  }

  if (!code) {
    return NextResponse.redirect(new URL('/login', appUrl))
  }

  const cookieStore = await cookies()

  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll()
        },
        setAll(cookiesToSet) {
          cookiesToSet.forEach(({ name, value, options }) => cookieStore.set(name, value, options))
        },
      },
    }
  )

  const { error: exchangeError } = await supabase.auth.exchangeCodeForSession(code)

  if (exchangeError) {
    const loginUrl = new URL('/login', appUrl)
    loginUrl.searchParams.set(
      'error',
      'El enlace ha expirado. Inicia sesión con tu correo y contraseña.'
    )
    return NextResponse.redirect(loginUrl)
  }

  // Redirect to custom destination if provided, otherwise dashboard
  const next = searchParams.get('next')
  const destination = next?.startsWith('/') ? next : '/dashboard'
  return NextResponse.redirect(new URL(destination, appUrl))
}
